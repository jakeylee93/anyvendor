// Map category labels to the CategoryIcon keys for filtering
export const categoryMapping: Record<string, string> = {
  "bars": "Bars & Drinks",
  "catering": "Catering",
  "music": "Music & DJs",
  "photography": "Photography",
  "venues": "Venues",
  "entertainment": "Entertainment",
  "decor": "Decor & Flowers",
  "transport": "Transport",
  "cakes": "Cakes & Treats",
  "hire": "Equipment Hire",
};

// Get vendors by category key (from URL param)
export function getVendorsByCategory(categoryKey: string): Vendor[] {
  const label = categoryMapping[categoryKey];
  if (!label) return allVendors;
  return allVendors.filter((v) => v.category === label);
}

// Get active categories (only show categories that have vendors)
export function getActiveCategories(): { key: string; label: string; count: number }[] {
  const counts: Record<string, number> = {};
  allVendors.forEach((v) => {
    counts[v.category] = (counts[v.category] || 0) + 1;
  });

  return Object.entries(categoryMapping)
    .filter(([_, label]) => counts[label] && counts[label] > 0)
    .map(([key, label]) => ({ key, label, count: counts[label] }));
}
